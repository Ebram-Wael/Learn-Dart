void main(){
  //variables
  //String
  String name = "John"; 
  print("String: $name"); // output => String: John

  //Integer
  int age = 25; 
  print("Integer: $age"); // output => Integer: 25

  //Double
  double height = 1.75; 
  print("Double: $height"); // output => Double: 1.75

  //Boolean
  bool isStudent = true; 
  print("Boolean: $isStudent"); // output => Boolean: true
  
  //dynamic
  dynamic myVariable = "Hello"; 
  print("Dynamic: $myVariable"); // output => Dynamic: Hello
}